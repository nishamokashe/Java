
//WAP to calculate the area of Cylender 
class Cylender
{
     public static void main(String[] args) 
    {
        // Area of Cylender (Area= 2*PI*radius*Heidth)
        double pi=3.14, r=4.5, h=2.2, area;
        area= (2*pi*r*h);
        System.out.println("\n\n Area of Cylender is"+area);
    }
}
